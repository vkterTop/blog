function extractKeywords(title, category) {
  const keywords = [category];
  keywords.push(title);
  const parts = title.split(/[^\u4e00-\u9fa5a-zA-Z0-9]+/);
  for (const p of parts) {
    const clean = p.trim();
    if (clean && !STOP_WORDS.has(clean) && clean.length >= 2) {
      keywords.push(clean);
    }
  }
  return [...new Set(keywords)].filter(k => k.length > 0).slice(0, 12);
}

function parseFile(filePath) {
  const content = fs.readFileSync(filePath, 'utf-8');
  const lines = content.split('\n');
  let category = '';
  if (lines[0].startsWith('# ')) {
    category = lines[0].replace('# ', '').trim();
  }
  const regex = /^##\s+(\d+)\.\s*(.+)$/mg;
  const matches = [...content.matchAll(regex)];
  const sections = [];
  for (let i = 0; i < matches.length; i++) {
    const m = matches[i];
    const num = m[1];
    const title = m[2].trim();
    const start = m.index + m[0].length;
    const end = (i < matches.length - 1) ? matches[i + 1].index : content.length;
    let body = content.slice(start, end).trim();
    body = body.replace(/^---\s*\n+/, '').replace(/\n+---\s*$/, '').trim();
    sections.push({
      category,
      title,
      content: body,
      keywords: extractKeywords(title, category)
    });
  }
  return sections;
}

function main() {
  if (!fs.existsSync(OUTPUT_DIR)) fs.mkdirSync(OUTPUT_DIR, { recursive: true });
  const files = fs.readdirSync(SOURCE_DIR)
    .filter(f => f.endsWith('.md') && !f.startsWith('_'))
    .sort();
  let total = 0;
  const summary = [];
  for (const file of files) {
    const filePath = path.join(SOURCE_DIR, file);
    const sections = parseFile(filePath);
    const baseName = path.basename(file, '.md');
    summary.push({ category: baseName, count: sections.length });
    for (const [idx, section] of sections.entries()) {
      const fileName = `${baseName}_${String(idx + 1).padStart(3, '0')}.json`;
      const outPath = path.join(OUTPUT_DIR, fileName);
      fs.writeFileSync(outPath, JSON.stringify(section, null, 2), 'utf-8');
      total++;
    }
  }
  fs.writeFileSync(path.join(OUTPUT_DIR, '_summary.json'), JSON.stringify(summary, null, 2), 'utf-8');
  console.log(`共输出 ${total} 个文件到 ${OUTPUT_DIR}`);
  console.log(JSON.stringify(summary, null, 2));
}

main();
